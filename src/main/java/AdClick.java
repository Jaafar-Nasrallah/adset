import java.util.Date;

public class AdClick {
    private String userId;
    private String adId;
    private String country;
    private Date date;

    public String getUserId() {
        return userId;
    }

    public String getAdId() {
        return adId;
    }

    public String getCountry() {
        return country;
    }

    public Date getDate() {
        return date;
    }
}
